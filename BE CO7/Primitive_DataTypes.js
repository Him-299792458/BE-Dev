//Primitive DataTypes:
// 1.) Number DataType:
let a=5;
console.log(a);

// 2.) String DataType:
let b="Himashu Kumar Singh";
console.log(b);

// 3.) Boolean DataType:
let c= true;
let d= false;
console.log(c);
console.log(d);

// 4.) Null DataType:(implies empty data)
let e=null;
console.log(e);

// 5.) Undefined DataType:(implies unknown data)
let f=undefined;
let g;
console.log(f);
console.log(g);


//We can reassign different datatypes to the same variable
// so a='Guddu' can be reassigned to a=5