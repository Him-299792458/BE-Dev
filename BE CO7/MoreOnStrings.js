// There are three ways of writing a string in JS.

//1.) ""(Quotation mark):
let a= "Guddu";
console.log(a);

//2.) ''(Single Quotation mark):
let b= 'Bhai';
console.log(b);

//3.) ``(Back ticks): 
let c= `You stick out of the crowd!
Baby it's a no brainer!`;
console.log(c);
// (3.) is used to write multi-line string inputs. The other two can only be used for a single-line quotation.